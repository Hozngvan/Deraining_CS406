class DRT_Inference_Wrapper(nn.Module):
    def __init__(self, dim=96, patch_size=1, l_recursion=3, n_RTB=6, num_heads=2, window_size=7):
        super().__init__()
        # Lưu cấu hình giống hệt trong notebook
        self.args = (dim, patch_size, l_recursion, n_RTB, num_heads, window_size)
        self.stored_state_dict = None

    def load_state_dict(self, state_dict, strict=True):
        self.stored_state_dict = state_dict

    def forward(self, x):
        B, C, H, W = x.shape
        # Khởi tạo model với resolution động, sử dụng đúng tham số từ notebook
        model = DeepRecursiveTransformer(
            dim=self.args[0], 
            input_resolution=(H, W), 
            patch_size=self.args[1], 
            l_recursion=self.args[2], 
            n_RTB=self.args[3], 
            num_heads=self.args[4], 
            window_size=self.args[5]
        )
        model.to(x.device)
        
        if self.stored_state_dict is not None:
            model.load_state_dict(self.stored_state_dict, strict=False)
        
        model.eval()
        return model(x)